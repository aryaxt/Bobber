import Foundation

extension UIColor {
    
    public class func primaryColor() -> UIColor {
        return UIColor(red: 26/255, green: 25/255, blue: 29/255, alpha: 1)
    }
    
    public class func whiteSmokeColor() -> UIColor {
        return UIColor(red: 245/255, green: 245/255, blue: 245/255, alpha: 1)
    }
	
	public class func deepSkyBlue() -> UIColor {
		return UIColor(red: 0/255, green: 191/255, blue: 255/255, alpha: 1)
	}
	
	public class func lightPurple() -> UIColor {
		return UIColor(red: 171/255, green: 130/255, blue: 255/255, alpha: 1)
	}
	
	public class func lightestGray() -> UIColor {
		return UIColor(red: 247/255, green: 247/255, blue: 247/255, alpha: 1)
	}
	
	public class func lighterGray() -> UIColor {
		return UIColor(red: 234/255, green: 234/255, blue: 234/255, alpha: 1)
	}
	
	public class func navy() -> UIColor {
		return UIColor(red: 52/255, green: 73/255, blue: 94/255, alpha: 1)
	}
	
	public class func blue() -> UIColor {
		return UIColor(red: 41/255, green: 128/255, blue: 185/255, alpha: 1)
	}
	
	public class func darkGreen() -> UIColor {
		return UIColor(red: 39/255, green: 174/255, blue: 96/255, alpha: 1)
	}
	
	public class func darkRed() -> UIColor {
		return UIColor(red: 192/255, green: 57/255, blue: 43/255, alpha: 1)
	}
	
}
